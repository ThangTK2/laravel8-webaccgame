    {
        $data = $request->all();
        $category = new Category();
        $category->title = $data['title'];
        $category->image = 'hinhanh1.jpg';
        $category->description = $data['description'];
        $category->status = $data['status'];
        $category->save();
        return redirect()->back();
    }

